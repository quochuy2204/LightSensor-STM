int calculate_lux(int adc_value) {
	int vol1, ldr_vol, lux, ldr_resistor;
	vol1 = adc_value / ADC_READING * REF_VOLTAGE;
	ldr_vol = REF_VOLTAGE - vol1;
	ldr_resistor = ldr_vol / vol1 * REF_RESISTANCE;
	lux = LUX_SCALAR * pow(ldr_resistor, LUX_CALC);
	return lux;
}
